        public static MediatypeInfo setObject(DataRow dr)
        {
            MediatypeInfo obj = new MediatypeInfo();
            if (dr.Table.Columns.Contains("id") && dr["id"] != DBNull.Value)//id
            {
                obj.Id = Convert.ToInt32(dr["id"]);
            }
            if (dr.Table.Columns.Contains("name") && dr["name"] != DBNull.Value)//名称
            {
                obj.Name = (dr["name"]).ToString();
            }
            if (dr.Table.Columns.Contains("des") && dr["des"] != DBNull.Value)//描述
            {
                obj.Des = (dr["des"]).ToString();
            }
            return obj;
        }
