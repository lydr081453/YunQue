        public static string getUpdateString(MediatypeInfo objTerm, MediatypeInfo Objupdate, string term, ref List<SqlParameter> ht, params SqlParameter[] param)
        {
            string sql = "update media_mediatype set id=@id,name=@name,des=@des where 1=1 ";
            SqlParameter param_id = new SqlParameter("@id", SqlDbType.Int, 4);
            param_id.Value = Objupdate.Id;
            ht.Add(param_id);
            SqlParameter param_name = new SqlParameter("@name", SqlDbType.NVarChar, 100);
            param_name.Value = Objupdate.Name;
            ht.Add(param_name);
            SqlParameter param_des = new SqlParameter("@des", SqlDbType.NVarChar, 1000);
            param_des.Value = Objupdate.Des;
            ht.Add(param_des);
            if (objTerm == null && (term == null || term.Trim().Length <= 0))
            {
                sql += " and id=@id ";
                if (ESP.Media.Access.Utilities.Common.Find(ht, "@id") == -1)
                {
                    SqlParameter p = new SqlParameter("@id", SqlDbType.Int, 4);
                    p.Value = Objupdate.Id;
                    ht.Add(p);
                }

            }
            if (objTerm != null)
            {
                sql += getTerms(objTerm, ref ht);
            }
            if (term != null && term.Trim().Length > 0)
            {
                sql += term;
            }
            if (param != null && param.Length > 0)
            {
                for (int i = 0; i < param.Length; i++)
                {
                    if (ESP.Media.Access.Utilities.Common.Find(ht, param[i].ParameterName) == -1)
                    {
                        ht.Add(param[i]);
                    }
                }
            }
            return sql;
        }


