        private static string getTerms(MediatypeInfo obj, ref List<SqlParameter> ht)
        {
            string term = string.Empty;
            if (obj.Id > 0)//id
            {
                term += " and id=@id ";
                if (ESP.Media.Access.Utilities.Common.Find(ht, "@id") == -1)
                {
                    SqlParameter p = new SqlParameter("@id", SqlDbType.Int, 4);
                    p.Value = obj.Id;
                    ht.Add(p);
                }
            }
            if (obj.Name != null && obj.Name.Trim().Length > 0)
            {
                term += " and name=@name ";
                if (ESP.Media.Access.Utilities.Common.Find(ht, "@name") == -1)
                {
                    SqlParameter p = new SqlParameter("@name", SqlDbType.NVarChar, 100);
                    p.Value = obj.Name;
                    ht.Add(p);
                }
            }
            if (obj.Des != null && obj.Des.Trim().Length > 0)
            {
                term += " and des=@des ";
                if (ESP.Media.Access.Utilities.Common.Find(ht, "@des") == -1)
                {
                    SqlParameter p = new SqlParameter("@des", SqlDbType.NVarChar, 1000);
                    p.Value = obj.Des;
                    ht.Add(p);
                }
            }
            return term;
        }
